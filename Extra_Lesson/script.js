let span = document.getElementsByTagName('SPAN');

for (let i = 0; i < span.length; i++) {
    span[i].addEventListener('click', function () {
        input.value = span[i].innerHTML;
        for (let k = 0; k < span.length; k++) {
            span[k].style.display = 'none';
        }
    })
}

input.addEventListener('input', function () {

    input.value = input.value.replace(/[^а-яА-Я]/ig, '');
    

    let firstLetter = input.value.slice(0,1);
    let otherLetters = input.value.slice(1);
    
    input.value = firstLetter.toUpperCase() + otherLetters.toLowerCase();
    
    for (let k = 0; k < countries.length; k++) {
        span[k].style.display = 'none';
    }
    for (let i = 0; i < countries.length; i++) {
        if (countries[i].startsWith(input.value) && input.value != '') {
            span[i].style.display = 'block';
        }
    }
});

